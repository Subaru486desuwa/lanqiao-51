#include <Seg.h>

unsigned char Wela[]={0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80};
unsigned char Dula[]={0xc0,0xf9,0xa4,0xb0,0x99,0x92,0x82,0xf8,0x80,0x90,0xff,0xc1}; //段码0-9 U

void Seg_Dis(unsigned char wela,dula,point)
{
	P0=0xff;
	P2=P2&0X1F|0XE0;
	P2&=0X1F;

	P0=Wela[wela];
	P2=P2&0X1F|0XC0;	
	P2&=0X1F;

	P0=Dula[dula];
	if(point)	P0&=0X7F;
	P2=P2&0X1F|0XE0;
	P2&=0X1F;
}